-- =========================
-- CREATE TABLE
-- =========================

CREATE TABLE PELANGGAN (
    ID_PELANGGAN INTEGER PRIMARY KEY,
    NAMA VARCHAR(50) NOT NULL,
    NO_HP VARCHAR(13),
    ALAMAT VARCHAR(50),
    EMAIL VARCHAR(100)
);

CREATE TABLE TRANSAKSI_SERVICE (
    ID_TRANSAKSI INTEGER PRIMARY KEY,
    ID_KENDARAAN INTEGER NOT NULL,
    TANGGAL_SERVICE DATE NOT NULL,
    TOTAL_BIAYA NUMERIC(10,2),
    STATUS_SERVICE VARCHAR(20),
    PELANGGAN_ID_PELANGGAN INTEGER NOT NULL,
    ID_PELANGGAN INTEGER NOT NULL
);

CREATE TABLE DETAIL_PRODUK (
    ID_DETAIL_PRODUCT INTEGER PRIMARY KEY,
    NAMA_PRODUCT VARCHAR(256) NOT NULL,
    HARGA NUMERIC(10,2) NOT NULL,
    STOK INTEGER NOT NULL,
    JENIS_PRODUCT VARCHAR(256) NOT NULL,
    TRANSAKSI_SERVICE_ID_TRANSAKSI INTEGER NOT NULL
);

CREATE TABLE AKSESORIS (
    ID_DETAIL_PRODUCT INTEGER PRIMARY KEY,
    WARNA VARCHAR(100) NOT NULL,
    MERK VARCHAR(100) NOT NULL,
    CONSTRAINT AKSESORIS_WARNA_UK UNIQUE (WARNA)
);

CREATE TABLE SPAREPART (
    ID_DETAIL_PRODUCT INTEGER PRIMARY KEY,
    MASA_PAKAI INTEGER NOT NULL,
    TIPE_KENDARAAN VARCHAR(100) NOT NULL,
    CONSTRAINT SPAREPART_MASA_PAKAI_UK UNIQUE (MASA_PAKAI)
);

CREATE TABLE KENDARAAN (
    ID_KENDARAAN INTEGER PRIMARY KEY,
    NOMOR_POLISI VARCHAR(50) NOT NULL,
    JENIS_KENDARAAN VARCHAR(50),
    WARNA VARCHAR(50),
    PELANGGAN_ID_PELANGGAN INTEGER NOT NULL
);

CREATE TABLE DETAIL_SERVICE (
    ID_DETAIL VARCHAR(50) PRIMARY KEY,
    ID_TRANSAKSI INTEGER NOT NULL,
    ID_LAYANAN INTEGER NOT NULL,
    JUMLAH INTEGER,
    SUBTOTAL INTEGER,
    LAYANAN_ID_LAYANAN INTEGER,
    TRANSAKSI_SERVICE_ID_TRANSAKSI INTEGER NOT NULL
);

CREATE TABLE LAYANAN (
    ID_LAYANAN INTEGER PRIMARY KEY,
    NAMA_LAYANAN VARCHAR(50) NOT NULL,
    HARGA NUMERIC(8,2) NOT NULL,
    DESKRIPSI VARCHAR(50),
    DURASI NUMERIC(3),
    DETAIL_SERVICE_ID_DETAIL VARCHAR(50) NOT NULL,
    ID_TRANSAKSI1 INTEGER NOT NULL,
    LAYANAN_ID_LAYANAN INTEGER NOT NULL
);

CREATE TABLE MONTIR (
    ID_MONTIR INTEGER PRIMARY KEY,
    NAMA_MONTIR VARCHAR(100) NOT NULL,
    NO_HP BIGINT NOT NULL,
    KEAHLIAN VARCHAR(100) NOT NULL,
    ID_SUPERVISOR INTEGER,
    TRANSAKSI_SERVICE_ID_TRANSAKSI INTEGER NOT NULL,
    MONTIR_ID_MONTIR INTEGER NOT NULL
);

CREATE TABLE BANK_TRANSFER (
    TRANSFER_ID INTEGER PRIMARY KEY,
    BANK_NAME VARCHAR(100) NOT NULL,
    ACCOUNT_NUMBER BIGINT NOT NULL
);

CREATE TABLE TUNAI (
    ID_PEMBAYARAN INTEGER PRIMARY KEY,
    UANG_DITERIMA NUMERIC(10,2) NOT NULL,
    KEMBALIAN NUMERIC(10,2) NOT NULL
);

CREATE TABLE PEMBAYARAN (
    ID_PEMBAYARAN INTEGER PRIMARY KEY,
    TRANSAKSI INTEGER NOT NULL,
    METODE_PEMBAYARAN VARCHAR(50) NOT NULL,
    TANGGAL_PEMBAYARAN DATE NOT NULL,
    JUMLAH NUMERIC(10,2),
    TRANSAKSI_SERVICE_ID_TRANSAKSI INTEGER NOT NULL,
    NOTE VARCHAR(256),
    BANK_TRANSFER_TRANSFER_ID INTEGER,
    TUNAI_ID_PEMBAYARAN INTEGER
);

  ALTER TABLE PEMBAYARAN
ADD CONSTRAINT ARC_1 CHECK (
    (
        TUNAI_ID_PEMBAYARAN IS NOT NULL
        AND BANK_TRANSFER_TRANSFER_ID IS NULL
    )
    OR
    (
        BANK_TRANSFER_TRANSFER_ID IS NOT NULL
        AND TUNAI_ID_PEMBAYARAN IS NULL
    )
);

-- =========================
-- INDEX
-- =========================

CREATE UNIQUE INDEX DETAIL_SERVICE_IDX
ON DETAIL_SERVICE(LAYANAN_ID_LAYANAN);

CREATE UNIQUE INDEX LAYANAN_IDX
ON LAYANAN(DETAIL_SERVICE_ID_DETAIL);

CREATE UNIQUE INDEX MONTIR_IDX
ON MONTIR(MONTIR_ID_MONTIR);

CREATE UNIQUE INDEX PEMBAYARAN_IDX
ON PEMBAYARAN(TRANSAKSI_SERVICE_ID_TRANSAKSI);

